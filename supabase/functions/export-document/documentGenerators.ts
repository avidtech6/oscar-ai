// Generate Markdown document
export async function generateMarkdown(content: ExportRequest['content']): Promise<Uint8Array> {
  const markdown = `
# ${content.title}

${content.author ? `**Author:** ${content.author}\n` : ''}
${content.date ? `**Date:** ${content.date}\n` : ''}

${content.metadata && Object.keys(content.metadata).length > 0 ? 
  Object.entries(content.metadata).map(([key, value]) => `**${key}:** ${value}\n`).join('') + '\n' : ''}

${content.sections.map(section => 
  `${'#'.repeat(section.level + 1)} ${section.title}\n\n${section.content}\n\n`
).join('')}

---
*Generated by Oscar AI • ${new Date().toLocaleDateString()}*
  `.trim()
  
  return new TextEncoder().encode(markdown)
}